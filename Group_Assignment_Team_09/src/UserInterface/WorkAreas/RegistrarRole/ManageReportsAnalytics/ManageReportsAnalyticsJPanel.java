    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        // TODO add your handling code here:
        clearTables();
        String selectedReport = (String) cmbReportType.getSelectedItem();

        if (selectedReport == null || selectedReport.equals("-- Select Report --")) {
            JOptionPane.showMessageDialog(this, "Please select a report type.");
            return;
        }

        if (selectedReport.contains("Enrollment")) {
            loadEnrollmentReport();
        } else if (selectedReport.contains("GPA")) {
            loadGPAReport();
        }
    }//GEN-LAST:event_btnGenerateActionPerformed

    
    private void loadEnrollmentReport() {
        DefaultTableModel model = (DefaultTableModel) tblEnrollmentReport1.getModel();
        model.setRowCount(0);

        HashMap<String, CourseSchedule> schedules = business.getMasterCourseCatalog();
        int totalEnrollments = 0;

        for (String semester : schedules.keySet()) {
            CourseSchedule cs = schedules.get(semester);

            for (CourseOffer co : cs.getSchedule()) {
                int enrolled = co.getEnrolledCount();
                String dept = co.getSubjectCourse().getCourseNumber().substring(0, 4); // example dept code

                Object[] row = new Object[5];
                row[0] = dept;
                row[1] = co.getCourseNumber();
                row[2] = co.getSubjectCourse().getName();
                row[3] = semester;
                row[4] = enrolled;
                model.addRow(row);

                totalEnrollments += enrolled;
            }
        }

        txtSummaryTitle.setText("Summary for Enrollment Report");
        txtTotalEnrollments.setText(String.valueOf(totalEnrollments));
        txtAverageGPA.setText("N/A");
    }

    // --- GPA Distribution Report ---
    private void loadGPAReport() {
        DefaultTableModel model = (DefaultTableModel) tblGPAReport.getModel();
        model.setRowCount(0);

        double totalGPA = 0.0;
        int studentCount = 0;
        Random random = new Random();

        for (StudentProfile sp : business.getStudentDirectory().getStudentList()) {
            double gpa = 2.5 + (random.nextDouble() * 1.5); // random GPA between 2.5â€“4.0
            String status = gpa >= 3.0 ? "Good Standing" : "Warning";

            Object[] row = new Object[5];
            row[0] = sp.getProgram() != null ? sp.getProgram() : "MSIS";
            row[1] = sp.getPerson().getPersonId();
            row[2] = sp.getPerson().getName();
            row[3] = String.format("%.2f", gpa);
            row[4] = status;
            model.addRow(row);

            totalGPA += gpa;
            studentCount++;
        }

        double avgGPA = (studentCount > 0) ? totalGPA / studentCount : 0.0;
        txtSummaryTitle.setText("Summary for GPA Report");
        txtTotalEnrollments.setText(String.valueOf(studentCount));
        txtAverageGPA.setText(String.format("%.2f", avgGPA));
    }

