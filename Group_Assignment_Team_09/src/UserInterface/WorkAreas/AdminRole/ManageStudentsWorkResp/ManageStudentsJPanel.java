    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String searchTerm = searchTextField.getText().trim();
        String searchType = (String) searchTypeComboBox.getSelectedItem();

        if (searchTerm.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter search term", "Search Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Clear table
        int rc = studentTable.getRowCount();
        for (int i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) studentTable.getModel()).removeRow(i);
        }

        StudentDirectory studentDir = business.getStudentDirectory();
        ArrayList<StudentProfile> results = new ArrayList<>();

        // Perform search based on type
        if (searchType.equals("Name")) {
            results = studentDir.searchByName(searchTerm);
        } else if (searchType.equals("University ID")) {
            StudentProfile sp = studentDir.searchByUniversityId(searchTerm);
            if (sp != null) {
                results.add(sp);
            }
        } else if (searchType.equals("Department")) {
            results = studentDir.searchByDepartment(searchTerm);
        }

        // Display results
        if (results.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No students found matching: " + searchTerm, "Search Results", JOptionPane.INFORMATION_MESSAGE);
        } else {
            for (StudentProfile sp : results) {
                Person p = sp.getPerson();
                Object[] row = new Object[5];
                row[0] = sp;
                row[1] = p.getUniversityId();
                row[2] = p.getName();
                row[3] = p.getEmail();
                row[4] = sp.getDepartment();

                ((DefaultTableModel) studentTable.getModel()).addRow(row);
            }

            JOptionPane.showMessageDialog(this, "Found " + results.size() + " student(s)", "Search Results", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

