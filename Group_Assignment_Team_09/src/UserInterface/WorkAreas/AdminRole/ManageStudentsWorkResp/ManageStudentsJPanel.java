    public void refreshTable() {
        // Clear table
        int rc = studentTable.getRowCount();
        for (int i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) studentTable.getModel()).removeRow(i);
        }

        StudentDirectory studentDir = business.getStudentDirectory();

        for (StudentProfile sp : studentDir.getStudentList()) {
            Person p = sp.getPerson();
            Object[] row = new Object[5];
            row[0] = sp;  // Store the object for later retrieval
            row[1] = p.getUniversityId();
            row[2] = p.getName();
            row[3] = p.getEmail();
            row[4] = sp.getDepartment();

            ((DefaultTableModel) studentTable.getModel()).addRow(row);
        }
    }

