    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if (selectedStudent == null) {
            JOptionPane.showMessageDialog(this, "Please select a student from the table", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Person p = selectedStudent.getPerson();
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this student?\n\n"
                + "University ID: " + p.getUniversityId() + "\n"
                + "Name: " + p.getName() + "\n"
                + "Email: " + p.getEmail() + "\n\n"
                + "This action cannot be undone!",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            StudentDirectory studentDir = business.getStudentDirectory();
            boolean deleted = studentDir.deleteStudent(selectedStudent);

            if (deleted) {
                JOptionPane.showMessageDialog(this, "Student deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                selectedStudent = null;
                refreshTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete student", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


